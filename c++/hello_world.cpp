#include <iostream>

int main() {
    std::cout << "Hello World!" << '\n'; // standard character output 
    std::cout << "My name is advay!" << '\n';
    std::cout << "This is so cool dude!" << std::endl;
    return 0;
}

/*
this is a comment in c++
studio character output = std:cout 
\n can be replaced by std:endl (studio endline)
newlines \n must be in quotations
*/